// ─── Model version selection ───────────────────────────────────────────────────

/**
 * A single selectable version of a model, surfaced in the model config panel.
 *
 * Atlas populates these on each ModelRegistryEntry. Aria renders them as a
 * version picker control inside the per-model settings panel. Gate persists the
 * user's selection via `selectedVersionId` on `ModelConfig`.
 *
 * `id` must be the exact API-level model string passed to the provider endpoint
 * (e.g. "claude-opus-4-5", "gpt-4o", "gemini-2.5-pro"). `displayName` is the
 * human-readable label shown in the picker. `description` is an optional
 * one-liner surfaced as a subtitle or tooltip.
 *
 * Design note: available versions are static (known at build time). This type
 * therefore lives on the MODEL_REGISTRY entry in Atlas, NOT on `ModelProvider`.
 * Adding `getAvailableVersions()` to `ModelProvider` would force every runtime
 * provider instance to carry static data it has no business owning, and would
 * complicate mocking in tests. If a future provider needs dynamic version
 * discovery (e.g. fetching from a versions endpoint), surface that as a
 * separate interface at that time — do not extend ModelProvider prematurely.
 */
export interface ModelVersionOption {
  /** API-level model string, e.g. "claude-opus-4-5", "gpt-4o". */
  id: string;
  /** Human-readable label shown in the version picker, e.g. "Claude Opus 4". */
  displayName: string;
  /** Optional one-liner shown as subtitle or tooltip, e.g. "Most capable". */
  description?: string;
}

// ─── Model config ─────────────────────────────────────────────────────────────

export interface ModelConfig {
  modelId: ModelId;
  /** Display name shown in UI. */
  name: string;
  /** Tailwind color token, e.g. "violet" or "sky". */
  color: string;
  /** Phase 2 — per-model system prompt. */
  systemPrompt?: string;
  isActive: boolean;
  /**
   * The `id` of the `ModelVersionOption` the user has selected for this model.
   * Absence means "use the provider's default version" (i.e. whatever Atlas
   * sends when no explicit model string override is applied).
   *
   * Gate reads and writes this field. Atlas reads it in `sendMessage` to choose
   * which API model string to pass to the provider endpoint. Aria reads it to
   * highlight the active option in the version picker.
   */
  selectedVersionId?: string;
}

// ─── Provider roster — Gate stores, Aria renders, Atlas dispatches ────────────

/**
 * Persisted configuration for a single built-in provider in the user's roster.
 *
 * Gate writes one of these per built-in provider the user has configured.
 * `isVisible` controls whether the provider appears in the Aria model selector
 * and can be added to new conversations — it does NOT remove the provider from
 * Atlas's dispatch table. A non-visible provider will simply never be active.
 *
 * `selectedVersionId` is the API-level model string the user last chose for
 * this provider (e.g. "claude-sonnet-4-6"). Absence means "use provider default".
 *
 * `credentialKey` is the `BuiltInCredentialKey` for the provider. Gate derives
 * this from `MODEL_CREDENTIAL_MAP` at write time so Atlas and Aria never need
 * to re-derive it.
 */
export interface BuiltInProviderConfig {
  kind: 'builtin';
  /** One of the six built-in model identifiers. */
  modelId: BuiltInModelId;
  /** The credential key used to retrieve the API key from Gate. */
  credentialKey: BuiltInCredentialKey;
  /**
   * The `id` of the `ModelCatalogEntry` the user has selected for this provider.
   * Absence means "use the provider's default version."
   */
  selectedVersionId?: string;
  /**
   * Whether this provider is visible in the Aria model selector.
   * Default: true. Users can set this to false to hide a built-in they do not use.
   */
  isVisible: boolean;
}

/**
 * Persisted configuration for a user-configured custom OpenAI-compatible provider.
 *
 * Gate writes one of these when the user adds a custom endpoint (OpenRouter,
 * Ollama, LM Studio, private endpoint, etc.). Atlas instantiates a dynamic
 * provider for each entry in the roster whose kind is 'custom'.
 *
 * `id` is a Gate-generated unique string (e.g. "custom-openrouter-1").
 * It serves as the `ModelId` for this provider everywhere in the system.
 * Gate must ensure no `id` collides with a `BuiltInModelId` value.
 *
 * `credentialKey` is Gate-generated and follows the pattern `"custom:<id>"`.
 * If the endpoint requires no authentication, `credentialKey` may be absent.
 *
 * `modelString` is the exact model identifier sent to the provider endpoint
 * (e.g. "meta-llama/llama-3-70b-instruct" for OpenRouter). This is distinct
 * from `id` — `id` identifies the provider config record; `modelString` is
 * the API parameter.
 *
 * `color` is an optional CSS custom-property token name (e.g. "accent-other")
 * or 6-digit hex string. Absence means Aria uses the 'accent-other' fallback.
 */
export interface CustomProviderConfig {
  kind: 'custom';
  /** Gate-generated unique identifier. This is the `ModelId` for this provider. */
  id: string;
  /** Human-readable label shown in the model selector and message bubbles. */
  displayName: string;
  /** Full URL of the OpenAI-compatible /chat/completions endpoint. */
  endpointUrl: string;
  /** Gate-generated credential key for this provider's API key (pattern: "custom:<id>"). */
  credentialKey?: string;
  /**
   * The model string sent to the provider endpoint (e.g. "llama-3-70b-instruct").
   * For providers like Ollama that use short names, this is exactly what they expect.
   */
  modelString: string;
  /**
   * Optional accent color. CSS custom-property token (e.g. "accent-other")
   * or 6-digit hex string (e.g. "#FF5500"). Aria applies 'accent-other' when absent.
   */
  color?: string;
  /**
   * Whether this provider requires an API key for authentication.
   *
   * When `false`, Gate skips the credential-present check before activating
   * the provider, and Atlas omits the `Authorization` header on all requests
   * to this provider's endpoint. Intended for no-auth providers such as Ollama
   * or other locally-hosted LLM servers that accept unauthenticated connections.
   *
   * When `true` or absent (the default), Gate enforces that a credential value
   * is stored for `credentialKey` before allowing the provider to be used.
   * Absence preserves existing behavior — all providers configured before this
   * field existed are treated as requiring a key.
   */
  requiresApiKey?: boolean;
}

/**
 * A single entry in the user's provider roster.
 * Discriminated on `kind` — 'builtin' or 'custom'.
 */
export type ProviderConfig = BuiltInProviderConfig | CustomProviderConfig;

/**
 * The user's complete configured provider roster.
 *
 * Gate owns storage (localStorage key: "roundtable:provider-roster").
 * Aria reads it to populate the model selector and conversation model list.
 * Atlas reads it to resolve which providers to instantiate and dispatch to.
 *
 * Ordering: the array order is the display order in the Aria model selector.
 * Gate may allow the user to reorder entries; Atlas must respect the order when
 * building the active provider list for a conversation.
 *
 * An empty roster is valid — the user has configured no providers yet.
 * Aria should surface a "configure providers" onboarding prompt in this state.
 */
export type ProviderRoster = ProviderConfig[];

// ─── Model catalog — model version entries from any source ────────────────────

/**
 * Provenance of a `ModelCatalogEntry` — where the version information came from.
 *
 * - 'bundled'  — shipped with Roundtable at build time (Atlas's static registry)
 * - 'remote'   — fetched from a remote `models.json` at runtime (raw.githubusercontent.com)
 * - 'live-api' — discovered via a provider's live models endpoint (e.g. OpenRouter /api/v1/models)
 */
export type ModelCatalogSource = 'bundled' | 'remote' | 'live-api';

/**
 * Normalized shape for a single selectable model version, regardless of source.
 *
 * This is the richer replacement for `ModelVersionOption`. It carries the same
 * `id`, `displayName`, and `description` fields as `ModelVersionOption`, plus
 * `source` for provenance tracking and an optional `contextWindow` for display.
 *
 * Atlas uses this in its dynamic registry (replacing the static `ModelVersionOption[]`
 * on `ModelRegistryEntry`) when it fetches versions from remote sources.
 * Aria renders these in the version picker the same way it renders `ModelVersionOption`.
 * Gate persists the selected `id` via `selectedVersionId` on `ProviderConfig`.
 *
 * `id` must be the exact API-level model string passed to the provider endpoint
 * (e.g. "claude-opus-4-8", "meta-llama/llama-3-70b-instruct").
 *
 * Backward compat: `ModelVersionOption` is retained for Atlas's static built-in
 * registry. New code that handles dynamic version lists should use `ModelCatalogEntry`.
 * Aria's version picker accepts either type by reading only `id`, `displayName`, and
 * `description` — both shapes are structurally compatible with those three fields.
 */
export interface ModelCatalogEntry {
  /** API-level model string, e.g. "claude-opus-4-8", "meta-llama/llama-3-70b-instruct". */
  id: string;
  /** Human-readable label shown in the version picker, e.g. "Claude Opus 4". */
  displayName: string;
  /** Optional one-liner shown as subtitle or tooltip, e.g. "Most capable". */
  description?: string;
  /** Where this entry originated. */
  source: ModelCatalogSource;
  /** Optional context window size in tokens, shown in the version picker if present. */
  contextWindow?: number;
}

