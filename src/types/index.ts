// ─── Primitives ───────────────────────────────────────────────────────────────

/**
 * The six built-in model identifiers that ship with Roundtable.
 *
 * This is a closed union — Atlas owns the concrete implementations for each
 * value. Gate owns the credential mapping for each value. Adding a new built-in
 * provider requires a types PR (Arch), an Atlas implementation, and a Gate
 * credential entry.
 *
 * **Runtime enumeration — single-source rule:** Any Gate module that needs a
 * runtime collection of these values (e.g. a `Set` for deserialization
 * validation, an array for iteration) must import from a single canonical Gate
 * utility rather than re-enumerating the string literals. Duplicating the list
 * across multiple files creates a silent drift hazard: adding a new member to
 * this union does not cause a compiler error in files that maintain their own
 * `Set`, so a new provider passes type-checking but fails runtime validation
 * silently. `MODEL_CREDENTIAL_MAP` in `@/auth/credentials` is the one allowed
 * exception — its `Record<BuiltInModelId, ...>` type enforces exhaustiveness
 * at compile time and is safe as a separate declaration.
 *
 * @see ModelId for the open union that includes user-defined custom providers.
 */
export type BuiltInModelId =
  | 'claude'
  | 'gpt-5.5'
  | 'gemini'
  | 'grok'
  | 'deepseek'
  | 'mistral';

/**
 * Identity token for any model — built-in or user-configured custom.
 *
 * `BuiltInModelId` members remain valid values and continue to receive IDE
 * autocomplete. Custom provider IDs are arbitrary strings chosen by Gate when
 * the user configures a new OpenAI-compatible endpoint (e.g. "openrouter-1",
 * "local-ollama"). Gate is responsible for generating unique IDs and for
 * ensuring no custom ID collides with a `BuiltInModelId` value.
 *
 * The `(string & {})` intersection is the TypeScript open-union idiom: it
 * preserves autocomplete on the named members while accepting any string.
 * Code that switches on known values (e.g. `case 'claude':`) continues to
 * type-check correctly. Code that needs to discriminate built-ins from custom
 * providers must use `BuiltInModelId` explicitly.
 *
 * Consumers must NOT use `ModelId` as an index type in a `Record<>` that
 * expects exhaustive coverage — use `BuiltInModelId` for that or a `Map`.
 */
export type ModelId = BuiltInModelId | (string & {});

export type InteractionMode = 'parallel' | 'manual' | 'auto-chain';

export type MessageRole = 'user' | 'assistant';

/**
 * Display metadata for a single interaction mode. Aria uses a typed registry
 * of these to render the mode-switcher control and tooltips (issue #12).
 */
export interface InteractionModeConfig {
  mode: InteractionMode;
  /** Short label shown in the switcher button. */
  label: string;
  /** One-sentence description shown in the tooltip. */
  description: string;
}

/**
 * A single step in an auto-chain sequence.
 * Defines which model speaks at this position and what role it plays.
 */
export interface ChainStep {
  /** Index of this step in the chain (0-based). */
  stepIndex: number;
  modelId: ModelId;
  /**
   * When true, this model's response is appended to the shared context before
   * the next step runs. When false the step runs in isolation.
   */
  appendToContext: boolean;
}

/**
 * Configuration for an auto-chain run. Atlas uses this to sequence
 * model calls; Aria may read it to render chain progress (issue #14).
 */
export interface AutoChainConfig {
  /** Ordered steps to execute. */
  steps: ChainStep[];
  /**
   * Maximum number of times the full step sequence may repeat before
   * the chain terminates automatically. 1 = single pass.
   */
  maxPasses: number;
}

/**
 * The six built-in credential keys, one per built-in provider.
 * Gate maps `BuiltInModelId` values to these keys in `MODEL_CREDENTIAL_MAP`.
 *
 * @see CredentialKey for the open union that includes user-defined keys.
 */
export type BuiltInCredentialKey =
  | 'anthropic'
  | 'openai'
  | 'google'
  | 'xai'
  | 'deepseek'
  | 'mistral';

/**
 * Identity token for an API credential — built-in or user-configured.
 *
 * Custom providers use credential keys of the form `"custom:<providerId>"`,
 * generated by Gate when the user configures a new endpoint. Gate is
 * responsible for storage and retrieval under these keys.
 *
 * Same open-union idiom as `ModelId`: named members get autocomplete;
 * arbitrary strings are accepted for custom provider keys.
 */
export type CredentialKey = BuiltInCredentialKey | (string & {});

export type ExportFormat = 'markdown' | 'html';

