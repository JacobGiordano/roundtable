// ‚îÄ‚îÄ‚îÄ Attachments ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

/**
 * A single file attachment on a user message. Phase 5 (issue #285).
 *
 * Only image MIME types are supported in the initial implementation. The union
 * is intentionally closed to the four types every vision-capable provider
 * accepts. Extending to other file types (PDF, etc.) requires a types PR when
 * a provider adds explicit support for them.
 *
 * `base64` is the raw base64-encoded file content ‚Äî no data-URL prefix
 * (i.e. no "data:image/jpeg;base64," preamble). Atlas prepends the appropriate
 * prefix when constructing provider-specific content parts.
 *
 * `sizeBytes` is the unencoded file size in bytes. Aria enforces a
 * per-attachment size ceiling before adding to the pending message; Atlas reads
 * this field for telemetry.
 *
 * `filename` is optional ‚Äî clipboard pastes carry no filename; file-picker
 * selections always carry one.
 *
 * Lifecycle:
 *   - Aria creates `Attachment` objects from the file picker / clipboard-paste
 *     handler and attaches them to the pending user message before calling
 *     `sendMessage`.
 *   - Atlas reads attachments from `SendMessageOptions.attachments` (the
 *     current in-flight turn) and from `Message.attachments` on prior messages
 *     in `conversation.messages` (conversation history). Atlas must not pass
 *     attachment content to a provider whose `capabilities.vision` is `false`
 *     or absent ‚Äî see `ProviderCapabilities.vision`.
 *   - Vault persists `Attachment` objects as part of `Conversation.messages`
 *     in localStorage; base64 content is stored verbatim. There is no separate
 *     blob store in Phase 5.
 */
export interface Attachment {
  id: string;
  mimeType: 'image/jpeg' | 'image/png' | 'image/gif' | 'image/webp';
  /** Raw base64-encoded content, without a data-URL prefix. */
  base64: string;
  /** Present when the attachment originated from the file picker; absent for clipboard pastes. */
  filename?: string;
  /** Unencoded file size in bytes. */
  sizeBytes: number;
}

// ‚îÄ‚îÄ‚îÄ Generated images ‚Äî model-returned image content ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

/**
 * A single image returned by a model in its response. Phase 5 (issue #363).
 *
 * This represents image content that a model generates or returns ‚Äî distinct
 * from `Attachment`, which represents images a user uploads with their message.
 * The directional difference is load-bearing: `Attachment` flows user ‚Üí model
 * (input); `GeneratedImage` flows model ‚Üí user (output).
 *
 * Structural consistency with `Attachment`:
 *   `base64` is the raw base64-encoded image content ‚Äî no data-URL prefix
 *   (i.e. no "data:image/png;base64," preamble). This matches the convention
 *   established by `Attachment.base64`. Atlas must strip any data-URL prefix
 *   before populating this field; Aria prepends the appropriate prefix when
 *   rendering the image in an `<img>` element.
 *
 * `mimeType` is an open `string` rather than a closed union. Unlike user
 * attachments ‚Äî where Roundtable controls which formats are accepted ‚Äî the
 * provider controls which formats it returns. Using a closed union here would
 * require a types PR each time a provider adds a new output format.
 * Aria's `<img>` element accepts any browser-supported MIME type.
 *
 * `altText` is an optional model-supplied description. When present, Aria
 * uses it as the `alt` attribute on the rendered `<img>` element. When absent,
 * Aria must supply a meaningful fallback (e.g. "Generated image") to maintain
 * WCAG 2.1 AA compliance ‚Äî never an empty `alt`.
 *
 * `width` and `height` are optional pixel dimensions as reported by the
 * provider. Aria uses these as `width` / `height` hints to prevent layout
 * shift while the image loads. When absent, Aria renders without explicit
 * dimensions.
 *
 * Lifecycle:
 *   - Atlas parses image content blocks from provider responses and creates
 *     `GeneratedImage` objects (issue #364). Atlas emits them via
 *     `StreamChunk.images` at stream completion.
 *   - Vault persists `GeneratedImage` objects as part of `Message.generatedImages`
 *     in localStorage; base64 content is stored verbatim (issue #365).
 *   - Aria reads `Message.generatedImages` and renders each image below the
 *     text content in assistant message bubbles (issue #366).
 */
export interface GeneratedImage {
  /** Stable identifier for this image, assigned by Atlas when parsing the response. */
  id: string;
  /** MIME type of the image, e.g. "image/png", "image/jpeg", "image/webp". Open string ‚Äî not a closed union. */
  mimeType: string;
  /** Raw base64-encoded image content, without a data-URL prefix. */
  base64: string;
  /** Optional description provided by the model. Aria uses this as the img alt attribute. */
  altText?: string;
  /** Optional image width in pixels, as reported by the provider. */
  width?: number;
  /** Optional image height in pixels, as reported by the provider. */
  height?: number;
}

// ‚îÄ‚îÄ‚îÄ Messages ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

export interface Message {
  id: string;
  role: MessageRole;
  content: string;
  /** Present on assistant messages. */
  modelId?: ModelId;
  timestamp: number;
  /** Populated when isDone is true on the final StreamChunk. */
  tokenUsage?: TokenUsage;
  /** Phase 2 ‚Äî directed replies. */
  targetModelId?: ModelId;
  /** True while the message is still streaming. */
  isStreaming?: boolean;
  /** Set on assistant messages when the stream terminated with an error. */
  error?: ModelError;
  /**
   * Attached files. Present only on user-role messages; always absent on
   * assistant-role messages. Atlas reads these when building multimodal
   * requests for vision-capable providers (Phase 5, issue #285).
   */
  attachments?: Attachment[];
  /**
   * Images returned by the model in this message. Present only on
   * assistant-role messages; always absent on user-role messages ‚Äî users
   * send attachments (input), models return generated images (output).
   * Atlas populates this from provider response image content blocks
   * (issue #364). Vault persists this alongside the rest of the message
   * (issue #365). Aria renders each image below the text content in the
   * assistant bubble (issue #366).
   */
  generatedImages?: GeneratedImage[];
}

// ‚îÄ‚îÄ‚îÄ Model version selection ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

/**
 * A single selectable version of a model, surfaced in the model config panel.
 *
 * Atlas populates these on each ModelRegistryEntry. Aria renders them as a
 * version picker control inside the per-model settings panel. Gate persists the
 * user's selection via `selectedVersionId` on `ModelConfig`.
 *
 * `id` must be the exact API-level model string passed to the provider endpoint
 * (e.g. "claude-opus-4-5", "gpt-4o", "gemini-2.5-pro"). `displayName` is the
 * human-readable label shown in the picker. `description` is an optional
 * one-liner surfaced as a subtitle or tooltip.
 *
 * Design note: available versions are static (known at build time). This type
 * therefore lives on the MODEL_REGISTRY entry in Atlas, NOT on `ModelProvider`.
 * Adding `getAvailableVersions()` to `ModelProvider` would force every runtime
 * provider instance to carry static data it has no business owning, and would
 * complicate mocking in tests. If a future provider needs dynamic version
 * discovery (e.g. fetching from a versions endpoint), surface that as a
 * separate interface at that time ‚Äî do not extend ModelProvider prematurely.
 */
export interface ModelVersionOption {
  /** API-level model string, e.g. "claude-opus-4-5", "gpt-4o". */
  id: string;
  /** Human-readable label shown in the version picker, e.g. "Claude Opus 4". */
  displayName: string;
  /** Optional one-liner shown as subtitle or tooltip, e.g. "Most capable". */
  description?: string;
}

