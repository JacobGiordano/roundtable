// ‚îÄ‚îÄ‚îÄ Provider roster ‚Äî Gate stores, Aria renders, Atlas dispatches ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

/**
 * Persisted configuration for a single built-in provider in the user's roster.
 *
 * Gate writes one of these per built-in provider the user has configured.
 * `isVisible` controls whether the provider appears in the Aria model selector
 * and can be added to new conversations ‚Äî it does NOT remove the provider from
 * Atlas's dispatch table. A non-visible provider will simply never be active.
 *
 * `selectedVersionId` is the API-level model string the user last chose for
 * this provider (e.g. "claude-sonnet-4-6"). Absence means "use provider default".
 *
 * `credentialKey` is the `BuiltInCredentialKey` for the provider. Gate derives
 * this from `MODEL_CREDENTIAL_MAP` at write time so Atlas and Aria never need
 * to re-derive it.
 */
export interface BuiltInProviderConfig {
  kind: 'builtin';
  /** One of the six built-in model identifiers. */
  modelId: BuiltInModelId;
  /** The credential key used to retrieve the API key from Gate. */
  credentialKey: BuiltInCredentialKey;
  /**
   * The `id` of the `ModelCatalogEntry` the user has selected for this provider.
   * Absence means "use the provider's default version."
   */
  selectedVersionId?: string;
  /**
   * Whether this provider is visible in the Aria model selector.
   * Default: true. Users can set this to false to hide a built-in they do not use.
   */
  isVisible: boolean;
  /**
   * Declared feature capabilities of this built-in provider. Phase 5 (issue #285).
   *
   * Design decision ‚Äî vision-capability cross-boundary resolution:
   *   Aria needs to show a pre-send warning modal when any active model lacks vision
   *   support ("Model X won't see your image"). Built-in capability defaults are
   *   authoritative in Atlas's domain, which Aria cannot import from. Rather than
   *   adding a runtime constant to this types file (which would violate the
   *   types-only rule) or having Aria maintain a second static copy, the solution
   *   is to add `capabilities` here ‚Äî mirroring the existing field on
   *   `CustomProviderConfig` ‚Äî and have Gate populate it from Gate's own static
   *   built-in capability map (following the BUILTIN_META precedent established
   *   in ProviderSettingsPanel). This gives Aria a uniform path to check
   *   `config.capabilities?.vision` for both built-in and custom providers without
   *   any cross-boundary import.
   *
   * Gate is responsible for writing this from its own BUILTIN_META-style capability
   * map when initializing or migrating the roster. Atlas also reads it ‚Äî when
   * `vision` is `true`, Atlas may include image content parts; when `false` or
   * absent, Atlas must send text-only requests to this provider.
   *
   * When absent (pre-migration records), Aria and Atlas apply the conservative
   * `ProviderCapabilities` defaults: `vision: false`, `toolUse: false`.
   * Gate must ensure this is populated for all six built-ins on roster init/migration.
   *
   * @see ProviderCapabilities for field-by-field semantics and defaults.
   * @see CustomProviderConfig.capabilities for the analogous field on custom providers.
   */
  capabilities?: ProviderCapabilities;
}

/**
 * Declared feature capabilities of a custom OpenAI-compatible provider endpoint.
 *
 * Not all endpoints that speak the OpenAI Chat Completions wire format implement
 * the full API surface. Some reject `stream_options`, some lack system-role
 * support, some never expose vision or function calling. This interface lets the
 * user declare ‚Äî at provider-configuration time ‚Äî which features their endpoint
 * actually supports. Atlas reads these declarations at request-build time and
 * omits unsupported parameters rather than probing for failures at runtime.
 *
 * All fields are optional. Absence of a field means "Atlas uses its built-in
 * default for this capability" ‚Äî Atlas owns and documents those defaults in
 * `/src/models/generic.ts`. Gate writes this object when the user configures or
 * updates a custom provider; Aria may expose capability toggles in the provider
 * settings panel.
 *
 * This is a pure static declaration ‚Äî not a runtime cache. Once Atlas reads it
 * on a given request, no further probing occurs.
 *
 * Design constraint: all fields are optional so existing `CustomProviderConfig`
 * records that predate this field remain valid without migration.
 */
export interface ProviderCapabilities {
  /**
   * Whether the endpoint supports `stream_options: { include_usage: true }` in
   * streaming requests (OpenAI Chat Completions format).
   *
   * When `true` (or absent), Atlas includes `stream_options` to request token
   * counts in the final SSE chunk. When `false`, Atlas omits `stream_options`
   * entirely ‚Äî no token counts are available for this provider session.
   *
   * Default (when absent): `true`. Optimistic ‚Äî most conformant endpoints
   * support this, and the current try/retry Option B behavior already handles
   * the error case gracefully. Explicit `false` replaces that probe with a
   * static skip, eliminating the one extra failed request per session.
   */
  streamUsage?: boolean;

  /**
   * Whether the endpoint accepts multimodal image inputs (vision).
   *
   * When `true`, Atlas may include `image_url` content parts in the messages
   * array. When `false` (or absent), Atlas must send text-only content ‚Äî passing
   * image parts to a non-vision endpoint produces an API error.
   *
   * Default (when absent): `false`. Conservative ‚Äî vision is a strict superset
   * of text completion; most custom endpoints serve text-only models even when
   * the underlying inference server is vision-capable. Explicit `true` opts in.
   */
  vision?: boolean;

  /**
   * Whether the endpoint supports function calling / tools.
   *
   * When `true`, Atlas may include a `tools` array in the request body and
   * handle `tool_calls` in the response. When `false` (or absent), Atlas must
   * not send function definitions ‚Äî endpoints that lack tool support return
   * errors or ignore the field silently.
   *
   * Default (when absent): `false`. Conservative ‚Äî function calling is an
   * optional extension; many lightweight inference servers and proxied endpoints
   * do not implement it.
   */
  toolUse?: boolean;

  /**
   * Whether the endpoint accepts a `system`-role message.
   *
   * When `true` (or absent), Atlas prepends the system prompt as a `role:
   * "system"` message in the messages array, which is the standard OpenAI
   * behavior. When `false`, Atlas must fold the system prompt into the first
   * user message or omit it ‚Äî some minimalist endpoints (certain Ollama
   * models, stripped-down inference servers) reject or silently drop system
   * messages.
   *
   * Default (when absent): `true`. Optimistic ‚Äî system-role support is
   * specified in the OpenAI Chat Completions format and nearly universally
   * implemented by conformant endpoints.
   */
  systemPrompt?: boolean;
}

/**
 * Persisted configuration for a user-configured custom OpenAI-compatible provider.
 *
 * Gate writes one of these when the user adds a custom endpoint (OpenRouter,
 * Ollama, LM Studio, private endpoint, etc.). Atlas instantiates a dynamic
 * provider for each entry in the roster whose kind is 'custom'.
 *
 * `id` is a Gate-generated unique string (e.g. "custom-openrouter-1").
 * It serves as the `ModelId` for this provider everywhere in the system.
 * Gate must ensure no `id` collides with a `BuiltInModelId` value.
 *
 * `credentialKey` is Gate-generated and follows the pattern `"custom:<id>"`.
 * If the endpoint requires no authentication, `credentialKey` may be absent.
 *
 * `modelString` is the exact model identifier sent to the provider endpoint
 * (e.g. "meta-llama/llama-3-70b-instruct" for OpenRouter). This is distinct
 * from `id` ‚Äî `id` identifies the provider config record; `modelString` is
 * the API parameter.
 *
 * `color` is an optional CSS custom-property token name (e.g. "accent-other")
 * or 6-digit hex string. Absence means Aria uses the 'accent-other' fallback.
 */
export interface CustomProviderConfig {
  kind: 'custom';
  /** Gate-generated unique identifier. This is the `ModelId` for this provider. */
  id: string;
  /** Human-readable label shown in the model selector and message bubbles. */
  displayName: string;
  /** Full URL of the OpenAI-compatible /chat/completions endpoint. */
  endpointUrl: string;
  /** Gate-generated credential key for this provider's API key (pattern: "custom:<id>"). */
  credentialKey?: string;
  /**
   * The model string sent to the provider endpoint (e.g. "llama-3-70b-instruct").
   * For providers like Ollama that use short names, this is exactly what they expect.
   */
  modelString: string;
  /**
   * Optional accent color. CSS custom-property token (e.g. "accent-other")
   * or 6-digit hex string (e.g. "#FF5500"). Aria applies 'accent-other' when absent.
   */
  color?: string;
  /**
   * Whether this provider requires an API key for authentication.
   *
   * When `false`, Gate skips the credential-present check before activating
   * the provider, and Atlas omits the `Authorization` header on all requests
   * to this provider's endpoint. Intended for no-auth providers such as Ollama
   * or other locally-hosted LLM servers that accept unauthenticated connections.
   *
   * When `true` or absent (the default), Gate enforces that a credential value
   * is stored for `credentialKey` before allowing the provider to be used.
   * Absence preserves existing behavior ‚Äî all providers configured before this
   * field existed are treated as requiring a key.
   */
  requiresApiKey?: boolean;
  /**
   * Declared feature capabilities of this provider endpoint.
   *
   * Optional ‚Äî absence means "no explicit overrides; Atlas uses its built-in
   * default for each capability." Gate writes this when the user configures or
   * later edits the provider. Atlas reads it at request-build time to decide
   * which parameters to include in the API call.
   *
   * Existing `CustomProviderConfig` records that predate this field are valid
   * without migration ‚Äî Atlas falls back to per-capability defaults on every
   * field that is absent.
   *
   * @see ProviderCapabilities for field-by-field semantics and defaults.
   */
  capabilities?: ProviderCapabilities;
}

