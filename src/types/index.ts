// ─── ConversationContext — Atlas's narrowed view of a conversation ────────────

/**
 * The slice of `Conversation` that Atlas is authorized to read when handling
 * a `sendMessage` call. It excludes Vault-internal and display-only fields
 * (`isGhost`, `archivedAt`, `groupId`, `title`, `createdAt`, `updatedAt`,
 * `interactionMode`) that Atlas has no business inspecting.
 *
 * Atlas reads: `id` (for telemetry/logging), `messages` (conversation history),
 * and `models` (for `systemPrompt` and `selectedVersionId` per model).
 *
 * Structural note: `Conversation` is a strict superset of `ConversationContext`,
 * so existing callers that pass a full `Conversation` to `sendMessage` continue
 * to compile without changes — TypeScript structural typing satisfies the
 * narrower type automatically. The restriction is enforced at the type level
 * only; no runtime transformation is needed.
 *
 * Aria passes the full active `Conversation`; tests may pass a minimal object
 * that satisfies only the three required fields.
 */
export interface ConversationContext {
  id: string;
  messages: Message[];
  /** Atlas reads `systemPrompt` and `selectedVersionId` per model entry. */
  models: ModelConfig[];
}

// ─── Streaming — sendMessage plumbing ─────────────────────────────────────────

export interface StreamChunk {
  modelId: ModelId;
  /** Incremental text delta. */
  content: string;
  isDone: boolean;
  /** Present only when isDone is true. */
  tokenUsage?: TokenUsage;
  error?: ModelError;
}

export type StreamHandler = (chunk: StreamChunk) => void;

// ─── sendMessage — Atlas exposes, Aria calls ──────────────────────────────────

export interface SendMessageOptions {
  conversationId: string;
  content: string;
  /** Phase 2 — omit to broadcast to all active models. */
  targetModelId?: ModelId;
  /** Phase 2 — omit for parallel/manual modes. Atlas uses this to sequence auto-chain calls. */
  chainConfig?: AutoChainConfig;
  /**
   * A narrowed view of the active conversation. Atlas reads `id`, `messages`,
   * and `models` (for `systemPrompt` and `selectedVersionId` per model). Vault-
   * internal and display-only fields (`isGhost`, `archivedAt`, `groupId`,
   * `title`, `createdAt`, `updatedAt`, `interactionMode`) are excluded from
   * this type — Atlas must not depend on them.
   *
   * When absent, Atlas falls back to the static PROVIDERS registry with no
   * per-model config resolution — a legacy/test path that remains valid.
   *
   * Aria should always pass the active conversation here. Absence is permitted
   * only for callers (e.g. tests) that intentionally bypass roster resolution.
   *
   * Structural compat: `Conversation` satisfies `ConversationContext`, so
   * existing callers passing a full `Conversation` continue to compile without
   * changes.
   */
  conversation?: ConversationContext;
  /**
   * Shared system prompt applied to all models in parallel/directed/auto-chain mode.
   *
   * When a model also has a `systemPrompt` set on its `ModelConfig` entry in
   * `conversation.models`, the per-model value takes precedence over this shared
   * value. Use this field when a single prompt should apply across all active
   * models and no per-model override is configured.
   *
   * Omit (or pass `undefined`) to use each model's own configured system prompt,
   * or no system prompt at all when no per-model value is set.
   */
  systemPrompt?: string;
  /**
   * Optional cancellation signal for the fan-out. When aborted, Atlas should
   * terminate all active provider streams as quickly as possible.
   *
   * Design note: this field lives on `SendMessageOptions` rather than on
   * `ModelProvider.sendMessage` because every concrete provider already uses
   * the fourth positional parameter slot for `selectedVersionId?: string`
   * (an Atlas-internal version-threading mechanism). Adding `signal` as a
   * fourth parameter on the public interface would make all six provider
   * implementations structurally incompatible with `ModelProvider`. Atlas
   * extracts this signal from `options` and threads it to individual providers
   * through its internal `VersionAwareProvider` type, which Atlas owns and may
   * extend freely.
   *
   * Aria passes this by creating an `AbortController`, storing the controller
   * as state, and passing `controller.signal` here. Aria calls `stopMessage()`
   * (which invokes `controller.abort()`) when the user clicks the stop button.
   */
  signal?: AbortSignal;
}

/**
 * Top-level send function. Fans out to all active ModelProviders in parallel.
 * Streams each model's response via onChunk. Resolves when all models are done.
 */
export type SendMessageFn = (
  options: SendMessageOptions,
  onChunk: StreamHandler
) => Promise<void>;

/**
 * Aborts all active streams for the current fan-out.
 *
 * Atlas implements this by signalling the AbortController(s) it created when
 * the paired `sendMessage` call was dispatched. Aria wires this to a stop
 * button in the input bar.
 *
 * Atlas provides a no-op stub before any stream is in flight; the real
 * implementation is installed at the moment `sendMessage` begins dispatching.
 * Calling `stopMessage` after all streams have already settled is safe and has
 * no effect.
 *
 * Aria: import this type and add `stopMessage: StopMessageFn` to
 * `RoundtableContextValue` so the stop button can call it.
 */
export type StopMessageFn = () => void;

// ─── ModelProvider — Atlas implements per model ───────────────────────────────

export type ModelErrorCode =
  | 'auth_failure'
  | 'rate_limit'
  | 'network_error'
  | 'context_length_exceeded'
  | 'unknown';

/**
 * Common base for all Roundtable error types. Aria uses this to write a
 * unified error boundary that catches `ModelError`, `StorageError`, and
 * `BackendAuthError` without separate catch branches.
 *
 * `source` is the discriminant — narrow to a concrete subtype using it:
 *   `if (err.source === 'model') { ... ModelError fields ... }`
 *
 * `code` is typed as `string` here to accommodate the different `code` unions
 * on each subtype. Concrete subtypes narrow `code` to their specific union.
 * `message` is the human-readable description of the error.
 */
export interface AppError {
  code: string;
  message: string;
  /**
   * Discriminant identifying which layer produced the error. Optional on the
   * base to remain backward-compatible with existing `ModelError` object
   * literals in Atlas that predate this unified type — those omit `source`
   * and are still valid `ModelError` values. New error construction sites
   * must always set `source`. Aria narrows on this field when present:
   * `if (err.source === 'model') { ... }`
   */
  source?: 'model' | 'storage' | 'auth';
}

/**
 * Error emitted by Atlas when a model call fails. The `modelId` field
 * identifies which model failed — relevant in parallel-broadcast mode where
 * one model's failure must not prevent others from continuing.
 *
 * Extends `AppError` with `source: 'model'` as the discriminant.
 */
export interface ModelError extends AppError {
  /**
   * Optional for backward compatibility with existing Atlas construction sites
   * that omit `source`. New code must always set `source: 'model'`. Aria
   * discriminates on this field when present.
   */
  source?: 'model';
  code: ModelErrorCode;
  message: string;
  /** Present when the error is attributable to a specific model. */
  modelId?: ModelId;
}

/**
 * Error emitted by Vault when a storage operation fails.
 *
 * Extends `AppError` with `source: 'storage'` as the discriminant.
 *
 *   - `quota_exceeded` — localStorage quota exhausted (QuotaExceededError)
 *   - `parse_failure`  — stored data present but corrupt (JSON.parse failure)
 *   - `not_found`      — requested conversation ID does not exist
 *   - `unknown`        — any other storage-layer error
 */
export interface StorageError extends AppError {
  source: 'storage';
  code: 'quota_exceeded' | 'parse_failure' | 'not_found' | 'unknown';
}

/**
 * Error emitted by Gate when an API key operation fails.
 *
 * Extends `AppError` with `source: 'auth'` as the discriminant.
 *
 *   - `invalid_key`  — key present in localStorage but rejected by the provider
 *   - `missing_key`  — no key stored for the requested credential
 *   - `expired_key`  — key was valid but has since expired (provider-reported)
 *   - `unknown`      — any other auth-layer error
 */
export interface BackendAuthError extends AppError {
  source: 'auth';
  code: 'invalid_key' | 'missing_key' | 'expired_key' | 'unknown';
}

export interface ModelProviderConfig {
  modelId: ModelId;
  name: string;
  color: string;
  /** The CredentialKey used to fetch the API key from Gate. */
  credentialKey: CredentialKey;
}

export interface ModelProvider {
  readonly config: ModelProviderConfig;
  /**
   * Sends messages to the underlying model and streams the response.
   * Resolves with token usage once the stream is complete.
   * Rejects if a non-recoverable error occurs (auth, network, etc.).
   *
   * Cancellation: stream cancellation is not surfaced as a parameter here
   * because all concrete providers already use the fourth positional slot
   * for `selectedVersionId` (an Atlas-internal concern). Atlas threads the
   * `AbortSignal` from `SendMessageOptions.signal` to individual providers
   * via its internal `VersionAwareProvider` type.
   */
  sendMessage(
    messages: Message[],
    systemPrompt: string | undefined,
    onChunk: StreamHandler
  ): Promise<{ tokenUsage?: TokenUsage }>;
}

// ─── StorageProvider — Vault implements ───────────────────────────────────────

/**
 * Serialized content produced by converting a conversation to a given format.
 * The caller decides what to do with it — trigger a download, stream over HTTP,
 * write to disk, etc.
 */
export interface ExportedConversation {
  /** The serialized conversation body (UTF-8 string). */
  content: string;
  /** Suggested filename including extension, e.g. "my-chat.md". */
  filename: string;
  /** MIME type of the content, e.g. "text/markdown;charset=utf-8". */
  mimeType: string;
}

/**
 * Core persistence contract. Vault owns the implementation; all other agents
 * interact with storage exclusively through this interface.
 *
 * Design invariants:
 *   - All methods are async. localStorage is synchronous under the hood, but
 *     the async boundary is load-bearing: it allows a future ServerStorageProvider
 *     to substitute without interface changes.
 *   - Ghost-mode guard: `saveConversation` silently no-ops when
 *     `conversation.isGhost === true`. All other write methods are not
 *     ghost-aware — callers must never pass a ghost conversation ID to them.
 *   - Upsert semantics: `saveConversation` creates or fully replaces a record.
 *     To update a single field (e.g. `groupId`), load the conversation, mutate
 *     the local copy, and call `saveConversation` with the full updated object.
 *     There is no `patchConversation`.
 *   - Idempotent deletes: `deleteConversation` on a non-existent ID is a no-op.
 *   - Export is side-effect-free: `exportConversation` returns serialized content
 *     and does NOT trigger a browser download. Download is a DOM concern that a
 *     ServerStorageProvider cannot fulfill. Vault's LocalStorageProvider wraps
 *     the return value in a download trigger; other consumers may stream it,
 *     write it to disk, or return it over HTTP.
 */
export interface StorageProvider {
  /**
   * Persist a conversation (create or replace — upsert semantics).
   *
   * Ghost-mode guard: if `conversation.isGhost === true`, returns immediately
   * without writing anything. This is the canonical guard point — callers do
   * not need to check `isGhost` before calling.
   *
   * Throws if the storage backend is full (e.g. QuotaExceededError in
   * localStorage). The caller is responsible for surfacing this to the user.
   */
  saveConversation(conversation: Conversation): Promise<void>;

  /**
   * Load a single conversation by ID.
   *
   * Returns `null` if no record exists for the given ID, or if the stored
   * data is present but corrupt (parse failure). Does not throw on missing or
   * corrupt data — callers should treat `null` as "not found".
   */
  loadConversation(id: string): Promise<Conversation | null>;

  /**
   * Return all stored conversations, sorted newest-first by `updatedAt`.
   *
   * Conversations whose underlying data is corrupt or missing are silently
   * omitted so the list remains usable even when storage is partially damaged.
   * Archived conversations (those with `archivedAt` set) are included —
   * filtering by archive status is the UI's responsibility.
   * Ghost conversations are never included (they are not persisted).
   */
  listConversations(): Promise<Conversation[]>;

  /**
   * Permanently remove a conversation from storage.
   *
   * No-op if the conversation does not exist. Removes the record from both
   * the index and its data key. Does not cascade to any related records.
   */
  deleteConversation(id: string): Promise<void>;

  /**
   * Mark a conversation as archived by setting its `archivedAt` timestamp.
   *
   * No-op if the conversation does not exist. Archived conversations remain
   * in the index and are returned by `listConversations` — they are not
   * deleted. The UI is responsible for showing/hiding archived conversations.
   * Group membership (`groupId`) is preserved on archive.
   *
   * To reverse, call `unarchiveConversation`.
   */
  archiveConversation(id: string): Promise<void>;

  /**
   * Clear the `archivedAt` timestamp on a conversation, returning it to
   * active status.
   *
   * No-op if the conversation does not exist or was not archived.
   */
  unarchiveConversation(id: string): Promise<void>;

  /**
   * Serialize a conversation to the requested format and return the result.
   *
   * Returns `null` if the conversation does not exist.
   *
   * Side-effect-free: does NOT trigger a browser download. The caller decides
   * what to do with the content — Vault's LocalStorageProvider wraps this in a
   * download trigger; a ServerStorageProvider would stream it over HTTP.
   * The `filename` and `mimeType` fields are hints that callers may override.
   */
  exportConversation(id: string, format: ExportFormat): Promise<ExportedConversation | null>;
}

